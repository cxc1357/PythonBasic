# day28：0-1 背包问题
# 物品价值
a1 = [100,70,50,10]
# 物品重量
a2 = [10,4,6,12]
# 背包容量
w = 12
# 求最大重量
def f(i,w):
    # 背包容量为0，或没有物品可装入，终止
    if w==0 or i<0:
        return 0
    # a2[i]太重无法装入背包，考虑下一个物品
    elif a2[i] > w:
        return f(i-1,w)
    # a2[i]可装入背包，装还是不装，取决于那种情况重量最大    
    return max(a1[i]+f(i-1,w-a2[2]),f(i-1,w))

r = f(3,w)
print(r)